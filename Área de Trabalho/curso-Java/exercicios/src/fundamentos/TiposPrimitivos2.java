package fundamentos;

public class TiposPrimitivos2 {
	public static void main(String[] args) {
		//Informações do funcionário
		
		//Tipos numéricos inteiros
		byte anosDeEmpresa = 23;
		short numeroDeVoos = 542;
		int id = 56789;
		long pontosAcumulados = 3134845223L;//coloca o L no final para indicar que
											//não é o tipo primitivo padrão int
		
		//Tipos numéricos reais
		float salario = 11445.44F; //coloca o F para indicar que não é o tipo primitivo
								   //padrão double
		double vendasAcumuladas = 2991797103.01;
		
		//Tipo booleano
		boolean estaDeFerias = false; //true
		
		//Tipo caracter
		char status = 'A'; //status ativo, usa aspas simples
	}

}
