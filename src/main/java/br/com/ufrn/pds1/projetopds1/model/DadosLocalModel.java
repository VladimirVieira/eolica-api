package br.com.ufrn.pds1.projetopds1.model;

public class DadosLocalModel {
	private double fator1;
	private double fator2;
	private double latitude;
	private double longitude;
	private String data;
	
	
	
	public double getFator1() {
		return fator1;
	}
	public void setFator1(double fator1) {
		this.fator1 = fator1;
	}
	public double getFator2() {
		return fator2;
	}
	public void setFator2(double fator2) {
		this.fator2 = fator2;
	}
	public double getLatitude() {
		return latitude;
	}
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
}
